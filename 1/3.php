    echo 'Вам пора на пенсию', PHP_EOL;
}
elseif ($age>=1 and $age<=17) {
    echo 'Вам ещё рано работать', PHP_EOL; 
}
elseif ($age>=18) {
    echo 'Вам ещё работать и работать', PHP_EOL;
}

// 4. Задание: Дополните  конструкцию   if­elseif,  выводя фразу: “Неизвестный  возраст” 
// при условии, что значение переменной $age не попадет в вышеописанные диапазоны чисел 
    
if ($age<1) {
